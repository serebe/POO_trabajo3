
package desicion_secuencial.ep_19;



public class metodos {
     public static double perimetro(double L){
        double Peri;
        Peri=3*L;
        return Peri;
    } 
     public static double Area(double L){
        double A;
        A=Math.sqrt(3)*Math.pow(L,2)/4;
        return A;
    } 
     public static double Altura(double L){
        double h;
        h=Math.sqrt(3)*L/2;
        return h;
    } 
}